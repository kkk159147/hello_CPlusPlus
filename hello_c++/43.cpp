#include <iostream>

using namespace std;

int main()
{
	//버림
	cout << floor(1.1) << endl;
	cout << floor(2.3) << endl;
	cout << floor(40.5) << endl;
	cout << floor(55.7) << endl;
	cout << floor(100.9) << endl;

	//올림
	cout << ceil(1.1) << endl;
	cout << ceil(2.3) << endl;
	cout << ceil(40.5) << endl;
	cout << ceil(55.7) << endl;
	cout << ceil(100.9) << endl;

	return 0;

}